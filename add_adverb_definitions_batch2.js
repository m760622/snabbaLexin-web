// Read data.js
let dataContent = fs.readFileSync('./data.js', 'utf-8');

let updateCount = 0;

// Update each adverb's Arabic definition
Object.entries(adverbDefinitions).forEach(([id, arabicDef]) => {
    // Match pattern: "ID", "Adverb.", "Swedish", "Arabic", "",
    const pattern = new RegExp(
        `("${id}",\\s*"Adverb\\.?"?,\\s*"[^"]*",\\s*"[^"]*",\\s*)""`
    );

    if (pattern.test(dataContent)) {
        dataContent = dataContent.replace(pattern, `$1"${arabicDef}"`);
        updateCount++;
    }
});

if (updateCount > 0) {
    fs.writeFileSync('./data.js', dataContent);
    console.log(`✅ Updated ${updateCount} adverbs with Arabic definitions`);
} else {
    console.log('No updates were made. Pattern may not match.');
}

